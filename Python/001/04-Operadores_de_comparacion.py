a = 4
b = 6

# IGUAL
resultado = a == b
print(f'Resultado == : {resultado}')

# DIFETENTE
resultado = a != b
print(f'Resultado != : {resultado}')

# MAYOR QUE
resultado = a > b
print(f'Resultado > : {resultado}')

# MAYOR O IGUAL
resultado = a >= b
print(f'Resultado >= : {resultado}')

# MENOR QUE
resultado = a < b
print(f'Resultado < : {resultado}')

# MENOR O IGUAL
resultado = a <= b
print(f'Resultado: <= {resultado}')